// 8bit setter operator
func (c *GbCore) setter(val byte) func(byte) {

	var f func(byte) 

	switch val {
	// B
	case 0x04, 0x05, 0x06,
		0x40, 0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47:
		f = c.GbCpu.SetB
	// C
	case 0x0c, 0x0d, 0x0e, 
		0x38,
		0x48, 0x49, 0x4a, 0x4b, 0x4c, 0x4d, 0x4e, 0x4f,
		0xd8, 0xda, 0xdc:
		f = c.GbCpu.SetC
	// D
	case 0x14, 0x15, 0x16,
		0x50, 0x51, 0x52, 0x53, 0x54, 0x55, 0x56, 0x57:
		f = c.GbCpu.SetD
	// E
	case 0x1c, 0x1d, 0x1e,
		0x58, 0x59, 0x5a, 0x5b, 0x5c, 0x5d, 0x5e, 0x5f:
		f = c.GbCpu.SetE
	// H
	case 0x24, 0x25, 0x26,
		0x60, 0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67:
		f = c.GbCpu.SetH
	// L
	case 0x2c, 0x2d, 0x2e,
		0x68, 0x69, 0x6a, 0x6b, 0x6c, 0x6d, 0x6e, 0x6f:
		f = c.GbCpu.SetL
	// (HL)
	case 0x34, 0x35, 0x36,
		0x70, 0x71, 0x72, 0x73, 0x74, 0x75,       0x77,
		0xe9,
		0x22, 0x32:
		f = c.setHL
	// A
	case 0x0a,
		0x1a,
		0x2a,
		0x3a, 0x3c, 0x3d, 0x3e,
		0x78, 0x79, 0x7a, 0x7b, 0x7c, 0x7d, 0x7e, 0x7f, 
		0xc6, 0xce,
		0xde,
		0xf0, 0xf2, 0xfa:
		f = c.GbCpu.SetA

	// (BC)
	case 0x02:
		f = c.setBC
	// (DE)
	case 0x12:
		f = c.setDE
	// (C)
	case 0xe2:
		f = c.setC
	// (a16)
	case 0xea:
		f = c.setA16
	// (a8)
	case 0xe0:
		f = c.setA8


	}

	return f
}
