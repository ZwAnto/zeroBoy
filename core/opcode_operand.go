// 16bit setter operator
func (c *GbCore) setter16(val byte) func(uint16) {

	var f func(uint16) 

	switch val {
	// BC
	case 0x03, 0x0b, 0x01, 0xc1:
		f = c.GbCpu.SetBC
	// DE
	case 0x13, 0x1b, 0x11, 0xd1:
		f = c.GbCpu.SetDE
	// HL
	case 0x23, 0x2b, 0x21, 0xe1:
		f = c.GbCpu.SetHL
	// SP
	case 0x33, 0x3b, 0x31, 0xf9, 0xf1:
		f = c.GbCpu.SetSP
	case 0x08:
		f = c.setA1616
	}
	return f
}
